#include "ft_printf.h"

int	ft_putnbr(long n)
{
	int	i;
	char	c;

	i = 0;
	if (n < 0)
	{
		write(1, "-", 1);
		n = -n;
		i++;
	}
	if (n >= 10)
	{
		i += ft_putnbr(n / 10);
	}
	c = (n % 10) + '0';
	write (1, &c, 1);
	i++;
	return (i);
}
